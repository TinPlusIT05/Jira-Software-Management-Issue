package dxc.com.jira.soft.dashboard.dao.issue;

public class IssueData {
	private String namePriority;
	private String nameIssue;
	private float total;
	
	public String getNamePriority() {
		return namePriority;
	}
	public void setNamePriority(String namePriority) {
		this.namePriority = namePriority;
	}
	public String getNameIssue() {
		return nameIssue;
	}
	public void setNameIssue(String nameIssue) {
		this.nameIssue = nameIssue;
	}
	public float getTotal() {
		return total;
	}
	public void setTotal(float total) {
		this.total = total;
	}

	
	
}
